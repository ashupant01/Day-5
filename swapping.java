package logicalproblems;

public class swapping {

		public static void main(String[] args) {
			
			int num1 = 10;
			int num2 = 20;
			int temp;
			
			temp = num1;
			num1 = num2;
			num2 = temp;
			
			System.out.println("After swapping num1 is : "+num1);
			System.out.println("After swapping num2 is : "+num2);
		}
	}

